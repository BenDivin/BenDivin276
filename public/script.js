function arraysEqual(a,b){
  i = a.length;
  while(i--){
    if(a[i] != b[i]){
      return false;
    }
  }
  return true;
}

function winCheck(){
  if(arraysEqual(board,win)){
    message[0].textContent = "An extremely OK win.";
  }
}

function solveBoard(){
  i = win.length;
  count = 0;
  while(i--){
    if(win[i] != board[i]){
      if(board[i] == 0){
        board[i] == 1;
        cells[i].style.backgroundColor = 'blue';
        count++;
      } else if(board[i] == 1){
        board[i] == 0;
        cells[i].style.backgroundColor = '';
        count++;
      }
    }
  }
  if(count == 0){
    message[0].textContent = "It's already solved though?";
  } else {
    message[0].textContent = "Guess you couldn't figure it out huh?";
  }
}
